
#ifndef NCURSESOBJECT_H_
#define NCURSESOBJECT_H_

#include <ncurses.h>


class NCurses;



/*! ----------------------------------------------------------------------------------------
 * @brief:
 * -----------------------------------------------------------------------------------------
 * */
class NCursesObject {
public:
	/*! ----------------------------------------------------------------------------------------
	 * @brief:
	 * -----------------------------------------------------------------------------------------
	 * */
	bool isSelected;

	/*! ----------------------------------------------------------------------------------------
	 * @brief:
	 * -----------------------------------------------------------------------------------------
	 * */
	virtual int HandleCommand(long command) = 0;

	/*! ----------------------------------------------------------------------------------------
	 * @brief:
	 * -----------------------------------------------------------------------------------------
	 * */
	virtual void SetPosition(int y, int x);

	/*! ----------------------------------------------------------------------------------------
	 * @brief:
	 * -----------------------------------------------------------------------------------------
	 * */
	void Move(int y, int x);

	/*! ----------------------------------------------------------------------------------------
	 * @brief:
	 * -----------------------------------------------------------------------------------------
	 * */
	void SetParent(WINDOW *parent);

	/*! ----------------------------------------------------------------------------------------
	 * @brief:
	 * -----------------------------------------------------------------------------------------
	 * */
	NCursesObject();
	NCursesObject(WINDOW *parent);
	virtual ~NCursesObject();

	/*! ----------------------------------------------------------------------------------------
	 * @brief:
	 * -----------------------------------------------------------------------------------------
	 * */
	virtual void Refresh();

	/*! ----------------------------------------------------------------------------------------
	 * @brief:
	 * -----------------------------------------------------------------------------------------
	 * */
	virtual void Resize(size_t width, size_t height);

	/*! ----------------------------------------------------------------------------------------
	 * @brief:
	 * -----------------------------------------------------------------------------------------
	 * */
	int GetWidth() const;
	int GetHeight() const;

	/*! ----------------------------------------------------------------------------------------
	 * @brief:
	 * -----------------------------------------------------------------------------------------
	 * */
	int GetPositionX() const;
	int GetPositionY() const;

protected:
	/*! ----------------------------------------------------------------------------------------
	 * @brief:
	 * -----------------------------------------------------------------------------------------
	 * */
	WINDOW *win;
	size_t y, x;
	size_t height, width;

};

#endif /* NCURSESOBJECT_H_ */
