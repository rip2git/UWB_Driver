#ifndef DATACONFIG_H_
#define DATACONFIG_H_


#include "UserPackFW.h"



/*! ----------------------------------------------------------------------------------------
 * @brief:
 * -----------------------------------------------------------------------------------------
 * */
class DataConfig {
public:
	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	enum class STATE {
		ERROR = -1,
		AVAILABLE
	};

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	uint8_t Destination;
	uint16_t Parts;
	uint8_t BufferSize;
	uint8_t TransactionSize;
	uint16_t Transactions;
	uint16_t TransactionNum;
	uint8_t TrustPacks;
	uint8_t Repeats;

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	DataConfig();

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	void ReadConfig();

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	void ToStruct(const UserPackFW &pack);

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	void ToUserPackFW(UserPackFW &pack) const;

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	DataConfig::STATE GetState() const;

private:
	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	const uint8_t configSize = 11;

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	mutable DataConfig::STATE state;
};

#endif /* DATACONFIG_H_ */
