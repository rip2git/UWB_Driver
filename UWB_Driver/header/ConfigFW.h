#ifndef CONFIGFW_H_
#define CONFIGFW_H_


#include "UserPackFW.h"



/*! ----------------------------------------------------------------------------------------
 * @brief:
 * -----------------------------------------------------------------------------------------
 * */
struct ConfigFW {
	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	enum class STATE {
		ERROR = -1,
		AVAILABLE
	};

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	const uint8_t configSize = 15;

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	struct SW1000_Str {
		uint16_t 	DeviceID;
		uint16_t 	PAN_ID;
		uint8_t		nDevices;
		uint16_t	PollingPeriod;
	} SW1000;
	struct Token_Str {
		uint8_t		TimeSlotDurationMs;
	} Token;
	struct Ranging_Str {
		uint16_t	RespondingDelay;
		uint16_t	FinalDelay;
	} Ranging;
	struct Routing_Str {
		uint8_t		TransactionSize;
		uint8_t		TrustPacks;
		uint8_t		Repeats;
	} Routing;

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	void ToUserPackFW(UserPackFW &pack);

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	ConfigFW();

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	ConfigFW::STATE GetState() const;
private:

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	ConfigFW::STATE state;
};

#endif /* CONFIGFW_H_ */
