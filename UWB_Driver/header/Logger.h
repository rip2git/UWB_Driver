#ifndef LOGGER_H_
#define LOGGER_H_


#include <fstream>
#include <string>
#include "TON.h"


/*! ----------------------------------------------------------------------------------------
 * @brief: Provides logging
 * -----------------------------------------------------------------------------------------
 * */
class Logger : public std::fstream {
public:
	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	enum class MODE : int {
		OFF = 0,
		CONSOLE = 1,
		FILE = 2
	};

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	Logger() : std::fstream() {};

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	~Logger();

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	void SwitchMode(Logger::MODE mode);

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	void toNull();

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	void toConsole();

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	void toFile();

	/*! ------------------------------------------------------------------------------------
	 * @brief:
	 * -------------------------------------------------------------------------------------
	 * */
	Logger& operator<<(TON &t);
};



#endif
